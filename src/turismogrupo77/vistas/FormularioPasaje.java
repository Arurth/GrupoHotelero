    private void jBBuscarOrigenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBBuscarOrigenActionPerformed
        // TODO add your handling code here:
        Ciudad ciudadencontrada = new Ciudad();
        CiudadData city = new CiudadData();

        ciudadencontrada = city.buscarCiudad(jTOrigen.getText());

        if (ciudadencontrada != null) {
            jTOrigen.setText(ciudadencontrada.getNombre());
            jTProvinciaOrigen.setText(ciudadencontrada.getProvincia());
            jTIDciudadOrigen.setText(ciudadencontrada.getIdCiudad() + "");

            //JOptionPane.showMessageDialog(null,"Ciudad Origen Encontrada");
            jTOrigen.setEnabled(false);
            jTProvinciaOrigen.setEnabled(false);
            jTImporte.setEnabled(true);
            jCTransporte.setEnabled(true);
            jCTransporte.setSelectedIndex(0);
            jBGuardar.setEnabled(true);
            jBCancelar.setEnabled(true);

        } else {
            JOptionPane.showMessageDialog(null, "La ciudad no Existe");
        }

    }//GEN-LAST:event_jBBuscarOrigenActionPerformed

