    private void jBGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBGuardarActionPerformed
        // TODO add your handling code here:
        Pasaje pass = new Pasaje();
        CiudadData city = new CiudadData();
        int repetido = 0;
        Pasaje origen = new Pasaje();
        Pasaje destino = new Pasaje();
        PasajeData passData = new PasajeData();

        if (jTIDciudadOrigen.getText().equals("") || jTIDciudadDestino.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Debe Ingresar un Origen y un Destino");
            return;
        }

        repetido = passData.buscarPasajeRepetido(Integer.parseInt(jTIDciudadOrigen.getText()), Integer.parseInt(jTIDciudadDestino.getText()), jCTransporte.getSelectedItem()+"");
        
        if (repetido != 0) {
            pass = passData.buscarPasaje(repetido);
            pass.setImporte(Double.valueOf(jTImporte.getText()));
            pass.setEstado(jREstado.isSelected());
            pass.setTipoTransporte(jCTransporte.getSelectedItem()+"");
            passData.modificarPasaje(pass);
            jTIDpasaje.setText(pass.getIdPasaje()+"");
            return;
        }

        Ciudad ciudadOrigen = city.buscarCiudad(jTOrigen.getText());
        Ciudad ciudadDest = city.buscarCiudad(jTDestino.getText());
        pass = new Pasaje(jCTransporte.getSelectedItem() + "", Double.valueOf(jTImporte.getText()), ciudadOrigen, ciudadDest, jREstado.isSelected());
        jTIDpasaje.setText(passData.guardarPasaje(pass)+"");
        
    }//GEN-LAST:event_jBGuardarActionPerformed

